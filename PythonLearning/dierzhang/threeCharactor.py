#单引号定义发，使用单引号进行包围
name = 'helloworld'
print(type(name))

# 双引号定义发，
name1 = "helloworld1"
print(type(name1))

# 三引号定义法
name2 = """hello world 3"""
print(type(name2))

# 在字符串中包含双引号
name = '"hello world`"'
print(name)
# 在字符串中包含dan引号
name = "'helloworld'"
print(name)
# 使用转移字符\接触引号的作用
name = "\"黑马程序员\""
print(name)

name = '\'hello world\''
